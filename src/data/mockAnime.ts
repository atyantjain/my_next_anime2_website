// Simple mock recommendation engine
export function getMockRecommendations(
  title: string,
  topK: number,
  minScore?: number,
  sameGenreOnly?: boolean,
  weights?: { genres: number; themes: number; composer: number; mood: number; studio: number; synopsis: number }
): Array<typeof animeList[0] & { similarity_score: number }> {
  const selected = animeList.find((a) => a.title === title);
  if (!selected) return [];

  const w = weights || { genres: 3, themes: 2, composer: 3, mood: 3, studio: 1, synopsis: 2 };
  const totalWeight = w.genres + w.themes + w.composer + w.mood + w.studio + w.synopsis;

  let candidates = animeList
    .filter((a) => a.title !== title)
    .map((a) => {
      const genreOverlap = selected.genres.filter((g) => a.genres.includes(g)).length / Math.max(selected.genres.length, 1);
      const themeOverlap = selected.themes.filter((t) => a.themes.includes(t)).length / Math.max(selected.themes.length, 1);
      const composerMatch = a.composer === selected.composer ? 1 : 0;
      const moodMatch = a.mood === selected.mood ? 1 : 0;
      const studioMatch = a.studios === selected.studios ? 1 : 0;
      const synopsisSim = 0.3 + Math.random() * 0.4; // mock

      const similarity =
        (genreOverlap * w.genres +
          themeOverlap * w.themes +
          composerMatch * w.composer +
          moodMatch * w.mood +
          studioMatch * w.studio +
          synopsisSim * w.synopsis) /
        totalWeight;

      return { ...a, similarity_score: Math.round(similarity * 1000) / 1000 };
    });

  if (minScore !== undefined) {
    candidates = candidates.filter((a) => a.score >= minScore);
  }
  if (sameGenreOnly) {
    candidates = candidates.filter((a) => a.genres.some((g) => selected.genres.includes(g)));
  }

  return candidates.sort((a, b) => b.similarity_score - a.similarity_score).slice(0, topK);
}
